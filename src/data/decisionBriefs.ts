export function getDecisionBrief(
  countrySlug: string,
  pathwayKey?: string
): DecisionBrief | undefined {
  if (pathwayKey) {
    return BRIEFS.find(
      (b) => b.countrySlug === countrySlug && b.pathwayKey === pathwayKey
    );
  }
  return BRIEFS.find(
    (b) => b.countrySlug === countrySlug && !b.pathwayKey
  );
}

export function getDecisionBriefsForCountry(
  countrySlug: string
): DecisionBrief[] {
  const briefs = BRIEFS.filter((b) => b.countrySlug === countrySlug);
  return briefs.sort((a, b) => {
    const aIsCountry = !a.pathwayKey ? 0 : 1;
    const bIsCountry = !b.pathwayKey ? 0 : 1;
    return aIsCountry - bIsCountry;
  });
}
